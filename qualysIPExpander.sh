recorreLista(){
	for IP in $LISTA_IP; do
		if [[ $IP == *"-"* ]]; then
			expandeIP $IP
		else
			echo $IP
		fi
	done
}

expandeIP(){
	IP=$1
	SEGMENTO=$(echo $IP | cut -d'-' -f1 | cut -d'.' -f1,2,3)
	IP1=$(echo $IP | cut -d'-' -f1 | cut -d'.' -f4)
	IP2=$(echo $IP | cut -d'-' -f2 | cut -d'.' -f4)
	for i in $(seq $IP1 $IP2);do
		echo $SEGMENTO"."$i
	done
}

